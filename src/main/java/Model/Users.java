package Model;


public class Users {

    public int userID;
    public String username;
    public String userPassword;


    /**
     * @return the userID
     */
    public int getUserID(){
        return userID;
    }

    /**
     * @return the userName
     */
    public String getUsername() {
        return username;
    }

    /**
     * @return the userPassword
     */
    public String getUserPassword() {
        return userPassword;
    }
}
